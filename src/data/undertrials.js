export function getDaysInCustody(arrestDate) {
  const arrest = new Date(arrestDate);
  const today = new Date();
  return Math.floor((today - arrest) / (1000 * 60 * 60 * 24));
}

export function getAlertStatus(days) {
  if (days > 90) return 'red';
  if (days > 60) return 'yellow';
  return 'green';
}

export function getBailScore(prisoner) {
  let score = 50;
  const offenseMap = { minor: 20, bailable: 10, 'non-bailable': -15, heinous: -30 };
  score += offenseMap[prisoner.offense_type] || 0;
  const days = getDaysInCustody(prisoner.arrest_date);
  if (days > 300) score += 20;
  else if (days > 180) score += 15;
  else if (days > 90) score += 10;
  else if (days > 60) score += 5;
  if (prisoner.prior_record) score -= 20;
  if (!prisoner.has_lawyer) score += 8;
  const flightMap = { low: 10, medium: 0, high: -20 };
  score += flightMap[prisoner.flight_risk] || 0;
  return Math.max(0, Math.min(100, score));
}